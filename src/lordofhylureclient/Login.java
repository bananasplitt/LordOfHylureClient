    private void loginJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJButtonActionPerformed
        // TODO add your handling code here: Login, versuche Login
        meldungJLabel.setText("");
        if(passwortJPasswordField.getText().isEmpty() && benutzerJTextField.getText().isEmpty()) {
            meldungJLabel.setText("Keine Eingabe");
        } else if(passwortJPasswordField.getText().isEmpty()) {
            meldungJLabel.setText("Kein Passwort eingegeben");
        } else if(benutzerJTextField.getText().isEmpty()) {
            meldungJLabel.setText("Kein Benutzername eingegeben");
            passwortJPasswordField.setText("");
        }
        if(connector.getAuthentification(benutzerJTextField.getText(), passwortJPasswordField.getText())) {
            dispose();
            LordOfHylureClient.gui.setVisible(true);
        }
    }//GEN-LAST:event_loginJButtonActionPerformed

